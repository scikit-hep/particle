    """Lazily construct and cache the ``PDGID`` for a literal name."""
    try:
        pdgid = common_particles[name]
    except KeyError:
        raise AttributeError(f"module {__name__!r} has no attribute {name!r}") from None
    value = PDGID(pdgid)
